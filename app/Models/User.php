        'email_verified_at' => 'datetime',
        'password' => 'hashed',
    ];

    public function jwtTokens(): \Illuminate\Database\Eloquent\Relations\HasMany
    {
        return $this->hasMany(JwtToken::class, 'user_id', 'uuid');
